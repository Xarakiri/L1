func reverse(s string) string {
	// –î–µ–ª–∞–µ–º –∏–∑ —Å—Ç—Ä–æ–∫–∏, —Å–ª–∞–π—Å —Ä—É–Ω (–¥–ª—è —Ä–∞–±–æ—Ç—ã —Å —é–Ω–∏–∫–æ–¥)
	r := []rune(s)

	l := len(r)

	// –ü–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É
	for i := 0; i < l/2; i++ {
		r[i], r[l-i-1] = r[l-i-1], r[i]
	}
	return string(r)
}

